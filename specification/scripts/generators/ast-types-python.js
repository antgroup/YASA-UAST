import t from "../../dist/index.js";
import fs from "fs";
import path from "path";

const DISCLAIMER = `# NOTE: This file is autogenerated from specification. Do not modify directly.
`;

const PYTHON_KEYWORDS = new Set([
  "False",
  "None",
  "True",
  "and",
  "as",
  "assert",
  "async",
  "await",
  "break",
  "case",
  "class",
  "continue",
  "def",
  "del",
  "elif",
  "else",
  "except",
  "finally",
  "for",
  "from",
  "global",
  "if",
  "import",
  "in",
  "is",
  "lambda",
  "match",
  "nonlocal",
  "not",
  "or",
  "pass",
  "raise",
  "return",
  "try",
  "while",
  "with",
  "yield",
]);

const LEGACY_FIELD_ORDER = {};

const LEGACY_DEFAULTS = {
  WhileStatement: { isPostTest: false },
  FunctionDefinition: { id: null, modifiers: null },
  VariableDeclaration: { variableParam: false },
  PrimitiveType: { id: "PrimitiveType", typeArguments: null },
  ArrayType: { id: "ArrayType", size: null, typeArguments: null },
  MapType: { id: "MapType", typeArguments: null },
  DynamicType: { id: null, typeArguments: null },
  ScopedType: { id: null, scope: null, typeArguments: null },
};

export default function generateAstTypes(dir) {
  const lines = [];
  lines.push("from typing import Any, List, Optional, Union, Literal as TypingLiteral");
  lines.push("from dataclasses import dataclass, field");
  lines.push("from dataclasses_json import dataclass_json, config");
  lines.push("");
  lines.push(DISCLAIMER.trimEnd());
  lines.push("");

  lines.push("@dataclass_json");
  lines.push("@dataclass");
  lines.push("class Meta:");
  lines.push("    isConstructor: bool = False");
  lines.push("    isAsync: bool = False");
  lines.push("    decorators: Optional['Identifier'] = None");
  lines.push("    parameterKind: Optional[str] = None");
  lines.push("");

  lines.push("@dataclass_json");
  lines.push("@dataclass");
  lines.push("class SourceLocation:");
  lines.push("    start: 'Position' = None");
  lines.push("    end: 'Position' = None");
  lines.push("    sourcefile: str = None");
  lines.push("");

  lines.push("@dataclass_json");
  lines.push("@dataclass");
  lines.push("class Position:");
  lines.push("    line: int");
  lines.push("    column: int");
  lines.push("");

  lines.push("@dataclass_json");
  lines.push("@dataclass");
  lines.push("class BaseNode:");
  lines.push("    loc: Optional[SourceLocation]");
  lines.push("    _meta: Meta");
  lines.push("    type: str = field(init=False)");
  lines.push("");
  lines.push("    def __post_init__(self):");
  lines.push("        self.type = self.__class__.__name__");
  lines.push("");

  for (const type of Object.keys(t.NODE_FIELDS)) {
    lines.push("@dataclass_json");
    lines.push("@dataclass");
    lines.push(`class ${type}(BaseNode):`);

    const fields = sortFieldNames(Object.keys(t.NODE_FIELDS[type]), type);
    const overrideOrder = LEGACY_FIELD_ORDER[type];
    const sortedFields = overrideOrder
      ? overrideOrder.filter(fieldName => fields.includes(fieldName))
      : fields;
    let orderedFields = sortedFields;
    if (!overrideOrder) {
      const requiredFields = [];
      const optionalFields = [];
      for (const fieldName of sortedFields) {
        const fieldDef = t.NODE_FIELDS[type][fieldName];
        const typeAnnotation = stringifyValidatorPython(fieldDef.validate);
        const hasLegacyDefault = Object.prototype.hasOwnProperty.call(LEGACY_DEFAULTS[type] || {}, fieldName);
        const isOptional = fieldDef.optional || fieldDef.default != null || hasLegacyDefault;
        if (isOptional) optionalFields.push(fieldName);
        else requiredFields.push(fieldName);
      }
      orderedFields = [...requiredFields, ...optionalFields];
    }
    if (orderedFields.length === 0) {
      lines.push("    pass");
      lines.push("");
      continue;
    }

    let sawDefaultField = false;
    for (const fieldName of orderedFields) {
      const fieldDef = t.NODE_FIELDS[type][fieldName];
      let typeAnnotation = stringifyValidatorPython(fieldDef.validate);
      const hasLegacyDefault = Object.prototype.hasOwnProperty.call(LEGACY_DEFAULTS[type] || {}, fieldName);
      let isOptional = fieldDef.optional || fieldDef.default != null || hasLegacyDefault;
      if (overrideOrder && sawDefaultField && !isOptional) {
        isOptional = true;
      }
      if (isOptional && !typeAnnotation.startsWith("Optional[")) {
        typeAnnotation = `Optional[${typeAnnotation}]`;
      }

      const pythonFieldName = getPythonFieldName(fieldName);
      const metadata = pythonFieldName !== fieldName
        ? `metadata=config(field_name='${fieldName}')`
        : null;

      let defaultExpr = "";
      if (metadata) {
        if (fieldDef.optional || fieldDef.default != null) {
          defaultExpr = ` = field(default=${pythonLiteral(fieldDef.default)}, ${metadata})`;
        } else {
          defaultExpr = ` = field(${metadata})`;
        }
      } else if (hasLegacyDefault) {
        defaultExpr = ` = ${pythonLiteral(LEGACY_DEFAULTS[type][fieldName])}`;
      } else if (fieldDef.optional || fieldDef.default != null) {
        defaultExpr = ` = ${pythonLiteral(fieldDef.default)}`;
      } else if (overrideOrder && sawDefaultField) {
        defaultExpr = " = None";
      }

      if (defaultExpr) {
        sawDefaultField = true;
      }
      lines.push(`    ${pythonFieldName}: ${typeAnnotation}${defaultExpr}`);
    }
    lines.push("");
  }

  for (const aliasName of Object.keys(t.FLIPPED_ALIAS_KEYS).sort()) {
    appendAlias(lines, aliasName, t.FLIPPED_ALIAS_KEYS[aliasName]);
  }
  appendAlias(lines, "Node", t.TYPES.filter(type => !t.FLIPPED_ALIAS_KEYS[type]).sort());

  writeTo(dir, "asttype_generated.py", `${lines.join("\n")}\n`);
}

function stringifyValidatorPython(validator) {
  if (validator === undefined) {
    return "Any";
  }

  if (validator.each) {
    return `List[${stringifyValidatorPython(validator.each)}]`;
  }

  if (validator.chainOf) {
    return stringifyValidatorPython(validator.chainOf[1]);
  }

  if (validator.oneOf) {
    if (validator.oneOf.length === 0) return "Any";
    return `TypingLiteral[${validator.oneOf.map(pythonLiteral).join(", ")}]`;
  }

  if (validator.oneOfNodeTypes) {
    return unionOf(validator.oneOfNodeTypes.map(mapScalarType));
  }

  if (validator.oneOfNodeOrValueTypes) {
    return unionOf(validator.oneOfNodeOrValueTypes.map(mapScalarType));
  }

  if (validator.type) {
    return mapScalarType(validator.type);
  }

  if (validator.shapeOf) {
    return "dict";
  }

  return "Any";
}

function mapScalarType(type) {
  const builtins = {
    string: "str",
    number: "int",
    boolean: "bool",
    null: "None",
  };

  if (builtins[type]) {
    return builtins[type];
  }

  return `'${type}'`;
}

function unionOf(types) {
  const uniqueTypes = [...new Set(types.filter(Boolean))];
  if (uniqueTypes.length === 0) return "Any";
  if (uniqueTypes.length === 1) return uniqueTypes[0];
  return `Union[${uniqueTypes.join(", ")}]`;
}

function pythonLiteral(value) {
  if (value === undefined || value === null) return "None";
  if (typeof value === "boolean") return value ? "True" : "False";
  if (typeof value === "string") return JSON.stringify(value);
  return String(value);
}

function getPythonFieldName(fieldName) {
  if (PYTHON_KEYWORDS.has(fieldName)) {
    return `${fieldName}_`;
  }
  return fieldName;
}

function appendAlias(lines, aliasName, values) {
  lines.push(`${aliasName} = Union[`);
  values.forEach((value, index) => {
    const suffix = index === values.length - 1 ? "" : ",";
    lines.push(`    ${value}${suffix}`);
  });
  lines.push("]");
  lines.push("");
}

function sortFieldNames(fields, type) {
  return fields.sort((fieldA, fieldB) => {
    const indexA = t.BUILDER_KEYS[type].indexOf(fieldA);
    const indexB = t.BUILDER_KEYS[type].indexOf(fieldB);
    if (indexA === indexB) return fieldA < fieldB ? -1 : 1;
    if (indexA === -1) return 1;
    if (indexB === -1) return -1;
    return indexA - indexB;
  });
}

function writeTo(dir, filename, content) {
  const filePath = path.join(dir, filename);
  fs.writeFileSync(filePath, content);
  console.log(`File ${filename} written successfully.`);
}
