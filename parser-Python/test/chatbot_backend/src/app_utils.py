def llm_get_knowledge_check(answer: str, threshold: float = 0.9) -> bool:
    """
    Check whether the LLM has successfully found the relevant knowledge it needs

    Args:
        answer(str): the output of the LLM
        threshold(float): the threshold of similarity, default at 0.9

    Returns:
        bool: Whether the LLM has successfully found the relevant knowledge it needs
    """
    chinese = "对不起，我没有找到和您的问题相关的内容，请换一个问题，或者提供更详细的细节。"
    english = "I'm sorry, but I can't find the information you're asking for in the Alipay+ documents. " \
              "Could you please provide more details or check another source?"

    answer_embedding = aiutils.compute_text_embedding(answer)
    chinese_embedding = aiutils.compute_text_embedding(chinese)
    english_embedding = aiutils.compute_text_embedding(english)

    if aiutils.is_chinese(answer):
        similarity = aiutils.vector_similarity(answer_embedding, chinese_embedding)
    else:
        similarity = aiutils.vector_similarity(answer_embedding, english_embedding)

    app.logger.info(f"Answer similarity (GPT_FOUND_NO_KNOWLEDGE): {similarity}")

    if similarity >= threshold:
        return False
    return True


def set_ams_product(request):
    sub_product = set()

    try:
        # product and scneario are in same meaning
        request_product = request.get_json()["product"]
        if len(request_product) == 0:
            product = ["ALL"]
        else:

            GOL = ['Checkout Payment', 'Auto debit',
                   'Easy pay', 'Scan To Bind', 'Subscription']
            gol_sub_product = {
                "Checkout Payment": ['Payment'],
                "Auto debit": ['Authorization', 'Payment'],
                "Easy pay": ['Payment'],
                "Scan To Bind": ['Authorization', 'Payment'],
                "Subscription": ['Subscription'],
            }

            GOF = ['User-Presented Mode Payment',
                   'Order Code Payment', 'Entry Code Payment']

            gof_sub_product = {
                "User-Presented Mode Payment": ['Payment', 'Refund', 'Registration'],
                "Order Code Payment": ['Payment', 'Refund', 'Registration'],
                "Entry Code Payment": ['Payment', 'Refund', 'Registration']
            }

            product = set()
            for p in request_product:
                if p in GOL:
                    product.add("GOL")
                if p in GOF:
                    product.add("GOF")
            product.add("ALL")

            for p in request_product:
                if gol_sub_product.get(p):
                    for p1 in gol_sub_product.get(p):
                        sub_product.add(p1)
                if gof_sub_product.get(p):
                    for p1 in gof_sub_product.get(p):
                        sub_product.add(p1)

    except KeyError:
        product = ["ALL"]

    product = list(product)
    sub_product = list(sub_product)

    return product, sub_product


