def validate_input(func):
    @wraps(func)
    def wrapper(*args, **kwargs):
        app.logger.info(f"request is:{request}");

        content_type = request.headers.get('Content-Type')
        if content_type == 'application/json':
            request_body = request.get_json()
        elif content_type in ['application/x-www-form-urlencoded', 'multipart/form-data']:
            request_body = request.form.to_dict()
        else:
            request_body = {}
        if not request_body:
            app.logger.error('Request body is empty')
            return jsonify({'error': 'Invalid input data'}), 400

        # 检查 SQL 注入和 XSS 攻击
        for key, value in request_body.items():
            if isinstance(value, str):
                if any(pattern.search(value) for pattern in SQL_INJECTION_PATTERNS + XSS_PATTERNS):
                    app.logger.error(f'Suspected malicious content found in {key}')
                    return jsonify({'error': f'Suspected malicious content found in {key}'}), 400

        # 验证电子邮件格式
        email_fields = ["email", "user_email", "feedback_email"]
        for field in email_fields:
            if field in request_body and not EMAIL_REGEX.match(request_body[field]):
                app.logger.error(f'The format of the {field} is invalid')
                return jsonify({'error': f'The format of the {field} is invalid'}), 400

        # 验证枚举类型值
        for field, valid_values in ENUM_VALIDATION_RULES.items():
            if field in request_body and request_body[field] not in valid_values:
                app.logger.error(f'error:The value of parameter {field} is invalid')
                return jsonify({'error': f'The value of parameter {field} is invalid'}), 400

        # 确认 question_id 是数字
        if "question_id" in request_body:
            if not str(request_body["question_id"]).isdigit():
                app.logger.error(f'error:The value of parameter question_id is invalid')
                return jsonify({'error': 'The value of parameter question_id is invalid'}), 400

        return func(*args, **kwargs)
    return wrapper


