def find_relevant_knowledge(
        detected_products: list,
        question_original: str,
        filtered_document_embeddings: dict,
        es_mode=ES_MODE,
        *args, **kwargs
):
    """
    Find relevant knowledge from the knowledge base

    Args:
        detected_products(list): the detected products
        question_original(str): the question
        filtered_document_embeddings(dict): the filtered document embeddings
        es_mode(str): the mode of Elasticsearch, supports 'none', 'hybrid', 'bm25' and 'cos_sim'

    Returns:
        Relevant knowledge
    """
    query_for_doc_retrieval = kwargs.get('query_for_doc_retrieval')
    db_table = kwargs.get('db_table')
    role = kwargs.get('role')
    version = kwargs.get('version')
    robot_type = kwargs.get('robot_type', 'DocsAI')
    es_query_size = kwargs.get('es_query_size')

    if es_mode == 'none':
        if len(detected_products) > 0:
            relevant_knowledge = aiutils.order_document_sections_by_query_similarity(
                question_original, filtered_document_embeddings, MINIMAL_DOCUMENT_SIMILARITY_FOR_SPECIFIC_QUESTION)
        else:
            relevant_knowledge = aiutils.order_document_sections_by_query_similarity(
                question_original, filtered_document_embeddings, MINIMAL_DOCUMENT_SIMILARITY)

        return relevant_knowledge
    else:
        return find_relevant_knowledge_by_es(
            query_for_doc_retrieval=query_for_doc_retrieval,
            db_table=db_table,
            role=role,
            version=version,
            mode=es_mode,
            robot_type=robot_type,
            es_query_size=es_query_size
        )


def find_relevant_knowledge_by_es(query_for_doc_retrieval, db_table, role, version, mode='hybrid', **kwargs):
    """
    Use Elasticsearch to query the knowledge base

    Args:
        query_for_doc_retrieval(str): the user question
        db_table(str): the knowledge base name
        role(str): the role, optional
        version(str): the version of the knowledge base
        mode(str): the search mode of Elasticsearch, supports 'hybrid', 'bm25', 'cos_sim'

    Returns:
        the knowledge
    """

    robot_type = kwargs.get('robot_type', 'DocsAI')
    documentation_title = kwargs.get('documentation_title')
    client = pool.es_client
    es_query_size = kwargs.get('es_query_size', ES_QUERY_SIZE)
    if documentation_title:
        es_query_size = 16
        mode = 'title_query'

    filter_condition = [
        {"term": {"role.keyword": role}},
        {"term": {"document_version.keyword": version}},
    ]
    if robot_type in ['Debugger', 'Code Generator']:
        filter_condition.append({
            "bool": {
                "should": [
                    {"match_phrase": {"title": "API Reference"}},
                    {"match_phrase": {"title": "SDK Reference"}}
                ]
            }
        })

    def execute_query(query):
        resp_es = client.search(
            index=db_table,
            _source=['title', 'url', 'content', 'features'],
            size=es_query_size,
            query=query,
            request_timeout=30,
        )
        return [
            (
                hit['_score'],
                int(hit['_id']),  # ES的_id字段只能是字符串，后端需要的是整数
                hit['_source']['title'],
                hit['_source']['url'],
                hit['_source']['content'],
                str(hit['_source']['features'])
            ) for hit in resp_es['hits']['hits']]

    def bm25():
        query = {
            "bool": {
                "filter": filter_condition,
                "must": {
                    "match": {
                        "content": query_for_doc_retrieval
                    }
                }
            }
        }
        return execute_query(query)

    def cos_sim():
        query = {
            "script_score": {
                "query": {
                    "bool": {
                        "filter": filter_condition,
                        "must": {"match_all": {}}
                    }
                },
                "script": {
                    "source": "cosineSimilarity(params.query_vector, 'features')",
                    "params": {
                        "query_vector": aiutils.compute_text_embedding(query_for_doc_retrieval)
                    }
                }
            }
        }
        return execute_query(query)

    def title_query():
        query = {
            'bool': {
                'filter': filter_condition,
                'must': {
                    "match_phrase": {
                        "title": documentation_title
                    }
                }
            }
        }
        return execute_query(query)

    start_time = time.time()

    if mode == 'hybrid':
        results = list(set(bm25() + cos_sim()))
    elif mode == 'bm25':
        results = bm25()
    elif mode == 'cos_sim':
        results = cos_sim()
    elif mode == 'title_query':
        results = title_query()
    else:
        raise ValueError(
            f'The ES search mode `{mode}` is not supported yet, please choose one from `hybrid`, `bm25`, `cos_sim` and `api_query`.')

    end_time = time.time()
    # print(f'Time taken to ESDb to get knowledge: {end_time - start_time}s')
    app.logger.debug(f'Time taken to ESDb to get knowledge: {end_time - start_time}s')
    return results


def extract_keyframes(video_path, output_dir, ssim_threshold=0.95):
    """Extract keyframes from a video"""
    start_time = time.time()
    if not os.path.exists(output_dir):
        os.makedirs(output_dir)

    cap = cv2.VideoCapture(video_path)
    prev_frame = None
    frame_count = 0
    keyframe_count = 0

    app.logger.info(f'Extracting keyframes from video `{video_path}`...')
    while True:
        ret, frame = cap.read()
        if not ret:
            break

        gray_frame = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)

        if prev_frame is None:
            prev_frame = gray_frame
            keyframe_path = os.path.join(output_dir, f"keyframe_{keyframe_count:04d}.png")
            cv2.imwrite(keyframe_path, frame)
            keyframe_count += 1
            continue

        score = ssim(prev_frame, gray_frame)

        if score < ssim_threshold:
            keyframe_path = os.path.join(output_dir, f"keyframe_{keyframe_count:04d}.png")
            cv2.imwrite(keyframe_path, frame)
            keyframe_count += 1

        prev_frame = gray_frame
        frame_count += 1

    cap.release()
    end_time = time.time()
    app.logger.info(
        f"Extracted {keyframe_count} keyframes from {frame_count} frames. Saved to '{output_dir}'. Time taken: {(end_time - start_time):.2f} seconds.")


def extract_frames(video_path: str, interval=0.5):
    """
    Extract frames by specified interval from a video
    """
    video = cv2.VideoCapture(video_path)

    # Get the frames per second of the video
    fps = video.get(cv2.CAP_PROP_FPS)
    app.logger.info(f"Frames per second: {fps}")

    # Set the interval for saving frames (in seconds)
    next_frame_time = 0.0
    frame_extracted = 0

    while video.isOpened():
        success, frame = video.read()
        if not success:
            break

        # Get the current position of the video file in milliseconds
        current_time = video.get(cv2.CAP_PROP_POS_MSEC) / 1000.0

        # If the current time is greater than or equal to the next frame time, save the frame
        if current_time >= next_frame_time:
            yield frame
            next_frame_time += interval

            frame_extracted += 1

    video.release()
    app.logger.info(f'{frame_extracted} frames extracted.')


def compute_ssim(prev_frame, current_frame):
    """Calculate the structure similarity between two images"""
    return ssim(prev_frame, current_frame)


def deduplicate_frames(video_path: str, output_dir: str, ssim_threshold=0.95, num_threads=4):
    """De-duplicate frames from a video"""
    start_time = time.time()
    if not os.path.exists(output_dir):
        os.makedirs(output_dir)

    frames = []
    frame_count, keyframe_count = 0, 0
    for frame in extract_frames(video_path):
        gray_frame = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)
        frames.append((frame_count, gray_frame, frame))
        frame_count += 1

    with ThreadPoolExecutor(max_workers=num_threads) as executor:
        futures = []
        for i in range(0, len(frames)):
            prev_frame = frames[i - 1][1]
            current_frame = frames[i][1]
            futures.append(executor.submit(compute_ssim, prev_frame, current_frame))

        for i, future in tqdm(enumerate(futures)):
            score = future.result()

            if score < ssim_threshold and (i + 1) < len(frames):
                keyframe_path = os.path.join(output_dir, f"keyframe_{keyframe_count:04d}.png")
                cv2.imwrite(keyframe_path, frames[i + 1][2])
                keyframe_count += 1

    end_time = time.time()
    app.logger.info(
        f"Extracted {keyframe_count} keyframes from {frame_count} frames. Saved to '{output_dir}'. Time taken: {(end_time - start_time):.2f} seconds.")





def encode_image(image_path):
    """Encode the image to base64"""
    with open(image_path, "rb") as image_file:
        return base64.b64encode(image_file.read()).decode('utf-8')


# 编译正则表达式以提高匹配性能
SQL_INJECTION_PATTERNS = [
    # 常见的 SQL 注入关键字（' 或者 -- 注释等）
    re.compile(r"(?:')|(?:--)|(/\*(?:.|[\n\r])*?\*/)|(union\s+select)", re.IGNORECASE),
    # URL 编码的注入攻击，如 %27（单引号）%3D（等号）等
    re.compile(r"((\%27)+)|((\%25)+)|((\;)+)|(((\%3D)+))|((\-\-)+)", re.IGNORECASE),
    # URL 编码的特殊字符检测（: ; < = " ( ) + 等）
    re.compile(r"((\%3A)+)|((\%3B)+)|((\%3C)+)|((\%3D)+)|((\%20)+)|((\%22)+)|((\%28)+)|((\%29)+)|((\%2B)+)", re.IGNORECASE)
]

XSS_PATTERNS = [
    # 检测 <script> 标签的 XSS 注入
    re.compile(r"<script.*?>.*?</script>", re.IGNORECASE),
    # 检测 < 和 > 标签，防止 HTML 标签注入
    re.compile(r"(<|%3C).*?(/>|%3E)", re.IGNORECASE),
    # 检测常见的 JavaScript 警告框， 如 alert()
    re.compile(r"(alert\(.*?\))", re.IGNORECASE),
    # 检测 `javascript:` 协议注入
    re.compile(r"javascript:", re.IGNORECASE),
    # 检测 HTML 属性事件注入，当前仅限 onmouseover 和 onclick
    re.compile(r"on(?:mouseover|click)=", re.IGNORECASE),
    # 检测可能的 JavaScript 函数调用，如 eval(), setTimeout(), setInterval()
    re.compile(r"(eval\(|function\(|window\.)", re.IGNORECASE),
    # 检测 img 标签中的 onerror 属性
    re.compile(r"<img\s+[^>]*\s+onerror\s*=", re.IGNORECASE),
    # 检测带有 data: 协议的 JavaScript 或恶意数据载入
    re.compile(r"data\s*:\s*image\/*[a-zA-Z]*;base64,", re.IGNORECASE),
    # 检测 JavaScript 注入的锚标签：<a href="javascript:...">
    re.compile(r"href\s*=\s*\"javascript:", re.IGNORECASE),
    # 检测不良 HTML 标签注入，如 <object>, <iframe>, <form> 等
    re.compile(r"<(script|iframe|object|embed|form|input|img|svg|math|canvas|meta)[^>]*>", re.IGNORECASE),
    # 检测内联 JavaScript（例如，直接写在标签内的代码）
    re.compile(r"(document\.cookie|eval\(|setTimeout\(|setInterval\()"),
    # 捕捉任何含有 javascript: 的 HTML 属性值，防止注入
    re.compile(r"<([a-zA-Z]+)[^>]*\s+[^>]*=[^>]*javascript\s*:", re.IGNORECASE)
]

EMAIL_REGEX = re.compile(r"^[a-zA-Z0-9+_.-]+@[a-zA-Z0-9.-]+$")

