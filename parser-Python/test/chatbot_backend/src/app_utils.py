def get_similar_question_clustering(df, top_k=None):
    """
    Get similar question clustering

    Args:
        df(pd.DataFrame): the dataframe read from the sql query
        top_k(int | None): return the top k results

    Returns:
        list[tuple[list[tuple[str, int]], int]]: a list of tuples contain similar question clustering and the count

    Examples:
        Return: [([('hello', 8), ('你好', 1), ('Hello', 1)], 10),...]
    """
    # similar_questions_with_count = []
    try:
        df["embedding"] = df.embedding.apply(literal_eval).apply(np.array)  # convert string to numpy array
        matrix = np.vstack(df.embedding.values)

        # Use HDBSCAN for clustering
        hdb = HDBSCAN(min_samples=3, min_cluster_size=3)
        hdb.fit(matrix)

        df['cluster'] = hdb.labels_
        df = df.query('cluster != -1')

        question_clusters = []
        for c in df.cluster.unique():
            question_clusters.append(df.query(f'cluster == {c}').question.to_list())

        # Sort similar questions by question count
        similar_questions = [Counter(cluster).most_common() for cluster in question_clusters]
        similar_questions_with_count = sorted(
            [(questions, sum([num for _, num in questions])) for questions in similar_questions],
            key=lambda x: x[1],
            reverse=True
        )
    except Exception as e:
        app.logger.error(f"error when fetching similar questions: {e}")
        return None
    # finally:
    #     conn.close()

    if top_k:
        return similar_questions_with_count[:top_k]
    return similar_questions_with_count


def find_record_by_chat_id(chat_id):
    # if the question is not empty, check the chat_id is valid or not
    record = None
    if chat_id and chat_id != '':
        # handle the connection gracefully
        app.logger.info(f"used_connection:{pool.get_used_connection_count()}")
        try:
            con = pool.get_connection()
            cur: MySQLCursor = con.cursor(buffered=True)
            # fetch the chat_id and product_belongs_to from log_questions table
            # chat_id is used to detect and identify same dialogue and product_belongs_to is used to identify the scenario of the question
            cur.execute(
                'select chat_id, prompt, answer, product_belongs_to, role from log_questions where chat_id = %s',
                [chat_id])
            # fetch the latest record
            # if the chat_id is not found, the record will be None
            if cur.rowcount > 0:
                record = cur.fetchall()[-1]
        except Exception as e:
            app.logger.error("error when fetching the chat_id: " + str(e))
        finally:
            con.close()
    return record


